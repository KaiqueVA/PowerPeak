/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */
	uint8_t tamanho, cont = 0;
	uint16_t i;

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_USART1_UART_Init();
  MX_ADC1_Init();
  MX_TIM2_Init();
  /* USER CODE BEGIN 2 */
  HAL_DMA_Init(&hdma_adc1);


  Machine_state_t state = ADC_READ;

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  switch(state)
	  {
	  	  case ADC_READ:
	  		  if(cont == 0)
	  		  {
  		  		  HAL_ADC_Start_DMA(&hadc1, (uint32_t*)adc_dma, 1024);
  		  		  cont = 1;
	  		  }
			  if(flag_ADC_DMA == 1){
				  for(i = 0; i < 512; i++)
				  {
					  iData[i] = adc_dma[i*2];
					  vData[i] = adc_dma[i*2 + 1];
				  }
				  HAL_ADC_Stop_DMA(&hadc1);
				  cont = 0;
				  state = CALCULE_RMS;
			  }
	  		  break;
	  	  case CALCULE_RMS:
	  		  calcIrms(&calcule, 512, 90.91, iData);
	  		  calcVrms(&calcule, 512, vData);

	  		  flag_ADC_DMA = 0;
	  		  state = CALCULE_WATTS;
	  		  break;

	  	  case CALCULE_WATTS:
	  		  calcule.pVA = calcule.iRms * calcule.vRms;
	  		  state = DATA_TRATAMENT;
	  		  break;

	  	  case DATA_TRATAMENT:
	  		  data_processing(&calcule, &data, &tamanho);
	  		  state = SEND_DATA;
	  		  break;

	  	  case SEND_DATA:
	  		  HAL_UART_Transmit_DMA(&huart1, (uint8_t*)data, strlen(data));
	  		  HAL_Delay(500);
	  		  free(data);
	  		  state = ADC_READ;
	  		  //state = ADC_READ;
	  		  break;


	  }
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

